var d=(new Date()).getDay(); // Día de la semana actual

if (d === 0) {
    console.log('Domingo');
} else if (d === 1) {
    console.log('Lunes');
} else if (d === 2) {
    console.log('Martes');
} else if (d === 3) {
    console.log('Miércoles');
} else if (d === 4) {
    console.log('Jueves');
} else if (d === 5) {
    console.log('Viernes');
} else console.log('Sábado');
